#include <iostream>
#include <cmath>

using namespace std;

int main() {
    cout << 60 << endl;
    cout << -60 << endl;
    cout << 60.9884 << endl;
    cout << 60 + 13 << endl;
    cout << 60 / 13 << endl;
    cout << 60 % 13 << endl;
    cout << 60.0 / 13 << endl;
    cout << (4 + 3) * 10 << endl;
    cout << 4 + 3 * 10 << endl;

    cout << pow(2, 3) << endl;
    cout << sqrt(36) << endl;
    cout << round(4.5) << endl;
    cout << round(4.499999) << endl;
    cout << ceil(7.1) << endl;
    cout << floor(7.9) << endl;
    cout << fmax(2, 9) << endl;
    cout << fmin(2, 9) << endl;

    return 0;
}